package ConditionalsAndLoops;

public class whileloop3 {
    public static void main(String[] args) {

        /*

        While loop syntax:
        while (condition){
            body
        }

        */
        int num=1;
        while (num<=10){
            System.out.println(num);
            num++;
//            or num += 1;
        }
    }
}
