package ConditionalsAndLoops;

public class dowhileLoop4 {
    public static void main(String[] args) {
        /*
        Syntax for do while loop
        do {
            body
        }while (condition);

        */
        //difference is that whatever is in do body will execute once no matter what

        int n = 1;
        do{
            System.out.println("Hello World");
        }while(n!=1); //even tho condition contradicts it still prints once
    }
}
